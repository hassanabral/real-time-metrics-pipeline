_VALID_RUN_IDS = {run.id for runs in _RUNS.values() for run in runs}


class WandbMetricsSDK:
    """Main SDK client for streaming ML experiment metrics."""

    def __init__(self) -> None:
        self._validate_timestamps: deque[float] = deque()
        self._rate_lock = threading.Lock()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def list_active_runs(self, project_id: str) -> list[ActiveRun]:
        """
        Return the active runs for *project_id*.

        Simulates ~100 ms network latency.
        Raises ``ValueError`` if *project_id* is empty.
        """
        if not project_id:
            raise ValueError("project_id must not be empty")
        time.sleep(0.1)
        return list(_RUNS.get(project_id, []))

    def stream_run_metrics(self, run_id: str) -> Generator[Metric, None, None]:
        """
        Generator that yields :class:`Metric` objects for *run_id*.

        Each call yields 8-15 metrics with 50-200 ms between yields,
        then the generator is exhausted.

        Raises ``ValueError`` if *run_id* is empty or unknown.
        """
        if not run_id:
            raise ValueError("run_id must not be empty")
        if run_id not in _VALID_RUN_IDS:
            raise ValueError(f"Unknown run_id: {run_id!r}")

        num_metrics = random.randint(8, 15)
        for step in range(1, num_metrics + 1):
            metric_name = random.choice(_METRIC_NAMES)
            lo, hi = _METRIC_RANGES[metric_name]
            value = round(random.uniform(lo, hi), 6)
            yield Metric(
                run_id=run_id,
                step=step,
                metric_name=metric_name,
                value=value,
                timestamp=time.time(),
            )
            time.sleep(random.uniform(0.05, 0.2))

    def validate_metric(self, metric: Metric) -> bool:
        """
        Validate a metric's structure.

        Simulates ~50 ms processing time.

        **Rate limited** -- at most 20 calls per second.  Raises
        :class:`RateLimitError` if the limit is exceeded.

        Returns ``True`` if the metric is valid, ``False`` otherwise
        (~10 % chance of returning ``False``).
        """
        now = time.time()

        with self._rate_lock:
            # Purge timestamps older than 1 second
            while self._validate_timestamps and self._validate_timestamps[0] < now - 1.0:
                self._validate_timestamps.popleft()
            if len(self._validate_timestamps) >= 20:
                raise RateLimitError()
            self._validate_timestamps.append(now)

        time.sleep(0.05)

        # ~10 % chance of an "invalid" metric
        return random.random() > 0.10
